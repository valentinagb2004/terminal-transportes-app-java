    private void verInformacion(Caseta caseta){
        
        if(caseta.getAdminFlota() != null){
            txtCedula.setText(caseta.getAdminFlota().getCedula());
            txtNombre.setText(caseta.getAdminFlota().getNombre());
            txtEdad.setText(caseta.getAdminFlota().getEdad()+"");
            txtCorreo.setText(caseta.getAdminFlota().getCorreo());
            txtTelefono.setText(caseta.getAdminFlota().getNumTel());
            txtSueldo.setText(caseta.getAdminFlota().getSueldo()+"");
            txtContrasena.setText(caseta.getAdminFlota().getContraseña());

            txtNit.setText(caseta.getNit());
            txtNombreEmpresa.setText(caseta.getNombreEmpresa());
            txtValorArrendamiento.setText(caseta.getValorCanonArrendamiento()+"");
            txtCantidad.setText(caseta.getCantidadPlazasEstacionamiento()+"");
            txtTelefonoEmpresa.setText(caseta.getAdminFlota().getTelefonoEmpresa());
        }
    }
    
    private boolean validarCampos() {
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String edad = txtEdad.getText();
        String correo = txtCorreo.getText();
        String telefono = txtTelefono.getText();
        String sueldo = txtSueldo.getText();
        String contrasena = txtContrasena.getText();
        
        String nit = txtNit.getText();
        String nombreEmpresa = txtNombreEmpresa.getText();
        String valorArrendamiento = txtValorArrendamiento.getText();
        String cantidadPlazas = txtCantidad.getText();
        String telefonoEmpresa = txtTelefonoEmpresa.getText();

        if (cedula.trim().equals("") || nombre.trim().equals("") || edad.trim().equals("") || correo.trim().equals("") || telefono.trim().equals("")
                || sueldo.trim().equals("") || contrasena.trim().equals("") || nit.trim().equals("") || nombreEmpresa.trim().equals("")
                || valorArrendamiento.trim().equals("") || cantidadPlazas.trim().equals("") || telefonoEmpresa.trim().equals("")) {
            return false;
        }
        return true;
    }

