    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:

        if(validarCampos()){

            String cedula = txtCedula.getText();
            String nit = txtNit.getText();

            if(controladorCaseta.verificarExistenciaAdminFlota(cedula)){
                JOptionPane.showMessageDialog(null, "Este administrador de flota ya esta trabajando para una empresa");
                return;
            }

            if(controladorCaseta.verificarExistenciaEmpresa(nit)){
                JOptionPane.showMessageDialog(null, "Esta empresa ya se encuentra registrada en la terminal");
                return;
            }

            try {
                String nombre = txtNombre.getText();
                String anos = txtEdad.getText();
                int edad = Integer.parseInt(anos);
                String correo = txtCorreo.getText();
                String telefono = txtTelefono.getText();
                String salario = txtSueldo.getText();
                double sueldo = Double.parseDouble(salario);
                String contrasena = txtContrasena.getText();

                String nombreEmpresa = txtNombreEmpresa.getText();
                String valorArrendamiento = txtValorArrendamiento.getText();
                double valorArrenda = Double.parseDouble(valorArrendamiento);
                String cantidadPlazas = txtCantidad.getText();
                int cantidad = Integer.parseInt(cantidadPlazas);
                String telefonoEmpresa = txtTelefonoEmpresa.getText();
                String tipoUsuario = "ADMIN_FLOTA";
            
                util.validarEmail(correo);
                util.validarTelefono(telefono);
                util.validarTelefono(telefonoEmpresa);
                
                AdminFlota adminFlota = new AdminFlota(sueldo, telefonoEmpresa, edad, nombre, cedula, correo, telefono , tipoUsuario, contrasena);
                controladorCaseta.guardarDatosCaseta(adminFlota, nit, nombreEmpresa, valorArrenda, cantidad);
                controladorPrincipal.guardarCambios();
                JOptionPane.showMessageDialog(null, "Se guardaron los datos de la caseta exitosamente");
                
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Ingrese un numero valido");
            }
            catch (TelefonoValidoException | EmailValidoException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "todos los campos son obligatorios");
        }
    }//GEN-LAST:event_btnCrearActionPerformed

