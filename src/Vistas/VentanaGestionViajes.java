    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        
        if(validarCampos()){
            
            String origen = txtOrigen.getText();
            String destino = txtDestino.getText();
            String fechaSalida = txtSalida.getText();
            String fechaLlegada = txtLlegada.getText();
            String valor = txtValor.getText();
            Bus bus = (Bus) cbBus.getSelectedItem();
            String horaSalida = (String) cbHoraSalida.getSelectedItem();
            String horaLlegada = (String) cbHoraLlegada.getSelectedItem();
            
            try {
                util.validarFecha(fechaSalida);
                util.validarFecha(fechaLlegada);
                
                boolean esFechaMayorSalida = util.fechaMayorAlSistema(fechaSalida, horaSalida);
                boolean esFechaMayorLlegada = util.fechaMayorAlSistema(fechaLlegada, horaLlegada);
                if(!esFechaMayorSalida || !esFechaMayorLlegada){
                    JOptionPane.showMessageDialog(null, "Las fechas deben tener una fecha mayor a la actual");
                    return;
                }
                
                if(!util.esFechaInicioMenor(fechaSalida, horaSalida, fechaLlegada, horaLlegada)){
                    JOptionPane.showMessageDialog(null, "Las fecha de salida debe de ser menor a la fecha llegada");
                    return;
                }
                
                Double valorUnitario = Double.parseDouble(valor);
                Viaje viaje = new Viaje(origen, destino, fechaSalida, fechaLlegada, horaSalida, horaLlegada, bus, valorUnitario);
                controladorGestionViajes.agregarViaje(viaje);
                controladorPrincipal.guardarCambios();
                llenarTabla();
                JOptionPane.showMessageDialog(null, "El viaje se registro");
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Ingrese un numero valido");
            } catch (FormatoFechaException | BusInhabilitadoException ex) {
               JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "todos los campos son obligatorios");
        }
    }//GEN-LAST:event_btnCrearActionPerformed

