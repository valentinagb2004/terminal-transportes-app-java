    private void llenarComboBus(){
        Lista<Bus> lista = controladorGestionViajes.getListaBuses();
        for (int i = 0; i < lista.size(); i++) {
            cbBus.addItem(lista.get(i));
        }
    }
    
    private void llenarTabla() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Origen", "Destino", "Fecha Salida", "Fecha Llegada", "Valor unitario", "Placa Bus"});
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");
        Lista<Viaje> lista = controladorGestionViajes.getListaViajes();
        for (int i = 0; i < lista.size(); i++) {
            Viaje viaje = lista.get(i);
            model.addRow(new Object[]{
                viaje.getOrigen(),
                viaje.getDestino(),
                viaje.getFechaSalidaCompleta().format(formatter),
                viaje.getFechaLlegadaCompleta().format(formatter),
                viaje.getValorUnitario(),
                viaje.getBus().getPlaca()
            });
        }
        tabla.setModel(model);
    }
    
    private boolean validarCampos() { 
        String destino = txtDestino.getText();
        String fechaSalida = txtSalida.getText();
        String fechaLlegada = txtLlegada.getText();
        String valor = txtValor.getText();
        Bus bus = (Bus) cbBus.getSelectedItem();
        
        if (destino.trim().equals("") || fechaSalida.trim().equals("") || fechaLlegada.trim().equals("")
                || valor.trim().equals("") || bus == null) {
            return false;
        }
        return true;
    }

