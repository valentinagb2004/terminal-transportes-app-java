    private boolean validarCampos() {
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String correo = txtEmail.getText();
        String telefono = txtTelefono.getText();
        String contrasena = txtContrasena.getText();

        if (cedula.trim().equals("") || nombre.trim().equals("") || correo.trim().equals("") 
                || telefono.trim().equals("") || contrasena.trim().equals("") ) {
            return false;
        }
        return true;
    }
    
    
    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            if (validarCampos()) {
               String cedula = txtCedula.getText();
               String nombre = txtNombre.getText();
               String correo = txtEmail.getText();
               String telefono = txtTelefono.getText();
               String contrasena = txtContrasena.getText();
               
               util.validarEmail(correo);
               util.validarTelefono(telefono);
               
               Cliente cliente = new Cliente(0, nombre, cedula, correo, telefono, "CLIENTE", contrasena);
               controladorCliente.registrarCliente(cliente);
               controladorPrincipal.guardarCambios();
               JOptionPane.showMessageDialog(null, "El cliente ha sido registrado");
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese todos los campos");
            }
            
        } catch (ClienteExisteException | TelefonoValidoException | EmailValidoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed


