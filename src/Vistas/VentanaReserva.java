    private void llenarTablaViajes(String fechaSalida, String destino) {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Numero", "Origen", "Destino", "Fecha Salida", "Fecha Llegada", "Valor unitario", "Placa Bus", "Cupos disponibles"});
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");
        listaViajes = controladorReservas.listaTodosViajesDisponibles(fechaSalida, destino);
        for (int i = 0; i < listaViajes.size(); i++) {
            Viaje viaje = listaViajes.get(i);
            model.addRow(new Object[]{
                i,
                viaje.getOrigen(),
                viaje.getDestino(),
                viaje.getFechaSalidaCompleta().format(formatter),
                viaje.getFechaLlegadaCompleta().format(formatter),
                viaje.getValorUnitario(),
                viaje.getBus().getPlaca(),
                viaje.calcularCuposDisponibles()
            });
        }
        tabla.setModel(model);
    }
     
     private void llenarTablaReservasActivas() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Numero", "Fecha", "Cant: tiquetes" });
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");
        Lista<Reserva> lista = controladorReservas.listaTodasMisReservasActivas(clienteLogueado.getCedula());
        for (int i = 0; i < lista.size(); i++) {
            Reserva reserva = lista.get(i);
            model.addRow(new Object[]{
                i,
                reserva.getFechaReserva().format(formatter),
                reserva.getCantidadTiquetes()
            });
        }
        tablaReservas.setModel(model);
    }
    
    private boolean validarCampos() {
        String cantidad = txtCantidadTiquetes.getText();
        String numero = txtNumeroViaje.getText();

        if (cantidad.trim().equals("") || numero.trim().equals("")) {
            return false;
        }
        return true;
    }

