    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        String numeroViaje = txtNumeroViaje.getText().trim();
        if (!numeroViaje.equals("")) {
            
            try {
                int numero = Integer.parseInt(numeroViaje);
                Viaje viaje = listaViajes.get(numero);
                iniciarControladorReservas(viaje.getBus().getPlaca());
                
                Notificacion notificacion = controladorReservas.cancelarReservas(clienteLogueado, viaje);
                if(notificacion != null){
                    Cliente clienteNotificado = controladorPrincipal.buscarCliente(notificacion.getCedula());
                    clienteNotificado.agregarNotificacion(notificacion);
                }
                
                llenarTablaViajes("", "");
                llenarTablaReservasActivas();
                controladorPrincipal.guardarCambios();
                JOptionPane.showMessageDialog(null, "se cancelaron las reservas activas del viaje seleccionado");
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Ingrese un numero valido");
            }catch (ArrayIndexOutOfBoundsException ex) {
                JOptionPane.showMessageDialog(null, "Ingrese un numero valido de viaje");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione el numero de viaje que desea cancelar las reservas");
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        
        if(validarCampos()){
            
            String cantidad = txtCantidadTiquetes.getText();
            String numeroViaje = txtNumeroViaje.getText();
            try {
                int numero = Integer.parseInt(numeroViaje);
                int cantidadTiquetes = Integer.parseInt(cantidad);
                
                if(cantidadTiquetes <= 0){
                    JOptionPane.showMessageDialog(null, "Ingrese un numero mayor a cero");
                    return;
                }
                
                Viaje viaje = listaViajes.get(numero);
                iniciarControladorReservas(viaje.getBus().getPlaca());
                
                Reserva reserva = new Reserva(viaje.getCodigo(), clienteLogueado, true, LocalDateTime.now(), cantidadTiquetes);
                controladorReservas.crearReserva(reserva, viaje);
                llenarTablaViajes("", "");
                llenarTablaReservasActivas();
                controladorPrincipal.guardarCambios();
                JOptionPane.showMessageDialog(null, "Se guardo la reserva para el viaje seleccionado");
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Ingrese un numero valido");
            }catch (ArrayIndexOutOfBoundsException ex) {
                JOptionPane.showMessageDialog(null, "Ingrese un numero valido de viaje");
            } catch (SinCuposDisponiblesException | LimiteBusException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "todos los campos son obligatorios");
        }
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        // TODO add your handling code here:
        String fecha = txtFecha.getText().trim();
        String destino = txtDestino.getText().trim();
        
        if(!fecha.equals("")){
            try {
                util.validarFecha(fecha);
            } catch (FormatoFechaException ex) {
               JOptionPane.showMessageDialog(null, ex.getMessage());
               return;
            }
        }
        
        llenarTablaViajes(fecha, destino);
    }//GEN-LAST:event_btnFiltrarActionPerformed


